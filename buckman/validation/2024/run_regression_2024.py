def run_pipeline_2024() -> bool:
    """
    Execute the complete 2024 pipeline: steps 1-4.

    Pipeline order:
    1. step1_ingest_buckman_data.py --year 2024
    2. step2_update_modflow.py --year 2024
    3. step3_run_modflow.sh --year 2024 (includes sfmodflx_2245.exe)
    4. step4_generate_depletion_tables.py --year 2024

    Returns:
        True if pipeline completed successfully, False otherwise
    """
    print_header("STEP 2: RUN 2024 PIPELINE")

    os.chdir(PROJECT_ROOT)
    print(f"Working directory: {PROJECT_ROOT}")
    print()

    # Pipeline steps
    steps = [
        ("Step 1: Ingest data", ["python3", "step1_ingest_buckman_data.py", "--year", "2024"]),
        ("Step 2: Update MODFLOW", ["python3", "step2_update_modflow.py", "--year", "2024"]),
        ("Step 3: Run MODFLOW", ["bash", "step3_run_modflow.sh", "--year", "2024"]),
        ("Step 4: Generate tables", ["python3", "step4_generate_depletion_tables.py", "--year", "2024"]),
    ]

    for step_name, cmd in steps:
        print(f"\n--- {step_name} ---")
        print(f"Command: {' '.join(cmd)}")
        print()

        try:
            result = subprocess.run(
                cmd,
                capture_output=False,  # Let output stream to console
                text=True,
                timeout=3600,  # 1 hour timeout for MODFLOW
                cwd=PROJECT_ROOT
            )

            if result.returncode != 0:
                print_error(
                    f"{step_name} failed",
                    str(PROJECT_ROOT),
                    f"Exit code {result.returncode}",
                    "Exit code 0",
                    "Check step output above for details"
                )
                return False

            print(f"{step_name}: COMPLETED")

        except subprocess.TimeoutExpired:
            print_error(
                f"{step_name} timed out",
                str(PROJECT_ROOT),
                "Exceeded 1 hour timeout",
                "Completion within timeout",
                "MODFLOW may be stuck or system is overloaded"
            )
            return False

        except FileNotFoundError as e:
            print_error(
                f"Command not found: {step_name}",
                str(PROJECT_ROOT),
                str(e),
                "Executable in PATH",
                "Check that python3, bash, and dependencies are installed"
            )
            return False

    print()
    print("Pipeline completed successfully.")
    return True


