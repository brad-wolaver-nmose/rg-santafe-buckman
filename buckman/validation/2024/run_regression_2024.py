# Base directories (relative to this script's location)
SCRIPT_DIR = Path(__file__).parent.resolve()
PROJECT_ROOT = SCRIPT_DIR.parent.parent  # validation/2024/ -> project root

INPUTS_DIR = SCRIPT_DIR / "inputs"
EXPECTED_DIR = SCRIPT_DIR / "expected_outputs"
TOLERANCES_FILE = SCRIPT_DIR / "tolerances.yaml"

# Pipeline output locations (generated by step4)
PIPELINE_OUTPUT_DIR = PROJECT_ROOT / "output" / "depletion"


# =============================================================================
# HELPER FUNCTIONS
# =============================================================================

def print_header(title: str) -> None:
    """Print a formatted section header."""
    print()
    print("=" * 70)
    print(title)
    print("=" * 70)


def print_error(what_failed: str, location: str, actual: str, expected: str, context: str) -> None:
    """Print forensic-quality error message."""
    print(f"ERROR: {what_failed}")
    print(f"  Location: {location}")
    print(f"  Actual: {actual}")
    print(f"  Expected: {expected}")
    print(f"  Physical context: {context}")


