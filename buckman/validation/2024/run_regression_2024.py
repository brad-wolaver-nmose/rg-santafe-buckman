#!/usr/bin/env python3
"""
2024 Regression Harness for Buckman Wellfield Pipeline

This script runs the complete Buckman wellfield depletion pipeline using frozen 2024
inputs and compares outputs against expected values. It provides a high-confidence
verification that code changes haven't broken the compliance-critical calculations.

Scientific Basis:
- Uses known-good 2024 data as regression baseline
- Cell-by-cell comparison with tolerance-based matching
- SHA-256 hash verification ensures input integrity

Assumptions:
1. Frozen inputs in validation/2024/inputs/ are unmodified
2. Expected outputs in validation/2024/expected_outputs/ are validated
3. tolerances.yaml defines acceptance criteria per table
4. All pipeline dependencies (Wine, MODFLOW, etc.) are installed

Usage:
    python3 run_regression_2024.py           # Full regression test
    python3 run_regression_2024.py --verify  # Hash verification only
    python3 run_regression_2024.py --compare # Compare only (skip pipeline)

Returns:
    Exit code 0: All tests passed
    Exit code 1: One or more tests failed
"""

import argparse
