def load_tolerances() -> dict[str, Any]:
    """Load tolerance definitions from YAML file."""
    with open(TOLERANCES_FILE) as f:
        return yaml.safe_load(f)


def values_match(actual: float, expected: float, tol_config: dict) -> tuple[bool, str]:
    """
    Check if two values match within tolerance.

    Args:
        actual: Computed value
        expected: Expected value
        tol_config: Tolerance configuration dict

    Returns:
        (matches, reason) tuple
    """
    # Handle NaN
    if pd.isna(actual) or pd.isna(expected):
        return False, "NaN value encountered"

    abs_tol = tol_config.get("absolute_tolerance", 0.01)
    rel_tol = tol_config.get("relative_tolerance", 0.001)

    abs_diff = abs(actual - expected)

    # Absolute tolerance check
    if abs_diff <= abs_tol:
        return True, f"within absolute tolerance ({abs_diff:.6f} <= {abs_tol})"

    # Relative tolerance check (avoid division by zero)
    if expected != 0:
        rel_diff = abs_diff / abs(expected)
        if rel_diff <= rel_tol:
            return True, f"within relative tolerance ({rel_diff:.4%} <= {rel_tol:.4%})"

    return False, f"diff={abs_diff:.6f} ({abs_diff/abs(expected)*100:.2f}% of expected)" if expected != 0 else f"diff={abs_diff:.6f}"


def compare_excel_files(
    actual_path: Path,
    expected_path: Path,
    table_name: str,
    tol_config: dict
) -> dict[str, Any]:
    """
    Compare two Excel files cell-by-cell within tolerances.

    Args:
        actual_path: Path to generated Excel file
        expected_path: Path to expected Excel file
        table_name: Table identifier (e.g., "Table_1")
        tol_config: Tolerance configuration for this table

    Returns:
        Comparison results dict with status, failures, etc.
    """
    result = {
        "table": table_name,
        "status": "PASS",
        "failures": [],
        "total_cells": 0,
        "passed_cells": 0,
    }

    # Check files exist
    if not actual_path.exists():
        result["status"] = "FAIL"
        result["failures"].append(f"Generated file not found: {actual_path}")
        return result

    if not expected_path.exists():
        result["status"] = "SKIP"
        result["failures"].append(f"Expected file not found: {expected_path}")
        return result

    # Load Excel files
    try:
        actual_df = pd.read_excel(actual_path, sheet_name=0)
        expected_df = pd.read_excel(expected_path, sheet_name=0)
    except Exception as e:
        result["status"] = "FAIL"
        result["failures"].append(f"Error reading Excel files: {e}")
        return result

    # Get max_year filter if specified (for Table 5)
    max_year = tol_config.get("max_year")

    # Compare dimensions
    if actual_df.shape != expected_df.shape:
        # Try to handle year filtering for Table 5
        if max_year and "Year" in expected_df.columns:
            expected_df = expected_df[expected_df["Year"] <= max_year]
            if "Year" in actual_df.columns:
                actual_df = actual_df[actual_df["Year"] <= max_year]

        if actual_df.shape != expected_df.shape:
            result["status"] = "FAIL"
            result["failures"].append(
                f"Shape mismatch: actual {actual_df.shape} vs expected {expected_df.shape}"
            )
            return result

    # Cell-by-cell comparison
    for row_idx in range(len(expected_df)):
        for col_idx in range(len(expected_df.columns)):
            result["total_cells"] += 1

            expected_val = expected_df.iloc[row_idx, col_idx]
            actual_val = actual_df.iloc[row_idx, col_idx]

            # Skip comparison for non-numeric cells
            if not isinstance(expected_val, (int, float)):
                result["passed_cells"] += 1
                continue

            if pd.isna(expected_val) and pd.isna(actual_val):
                result["passed_cells"] += 1
                continue

            matches, reason = values_match(actual_val, expected_val, tol_config)

            if matches:
                result["passed_cells"] += 1
            else:
                result["status"] = "FAIL"
                col_name = expected_df.columns[col_idx]
                result["failures"].append(
                    f"Row {row_idx + 1}, Col '{col_name}': "
                    f"actual={actual_val:.6f}, expected={expected_val:.6f}, {reason}"
                )

    return result


def compare_tables() -> dict[str, Any]:
    """
    Compare all generated tables against expected outputs.

    Returns:
        Dict with overall status and per-table results
    """
    print_header("STEP 3: COMPARE TABLES")

    tolerances = load_tolerances()
    results = {
        "overall_status": "PASS",
        "tables": {},
    }

    # Table mapping: (generated_name, expected_name)
    table_mappings = [
        ("TABLE_3_Rio_Pojoaque_Tesuque_2024.xlsx", "Table_3_expected.xlsx", "Table_3"),
        ("TABLE_4_Rio_Grande_Otowi_2024.xlsx", "Table_4_expected.xlsx", "Table_4"),
        ("TABLE_5_La_Cienega_Springs_2024.xlsx", "Table_5_expected.xlsx", "Table_5"),
    ]

    for generated_name, expected_name, table_key in table_mappings:
        actual_path = PIPELINE_OUTPUT_DIR / generated_name
        expected_path = EXPECTED_DIR / expected_name

        tol_config = tolerances.get("tables", {}).get(table_key, {})

        print(f"\nComparing {table_key}...")
        print(f"  Generated: {actual_path}")
        print(f"  Expected:  {expected_path}")

        result = compare_excel_files(actual_path, expected_path, table_key, tol_config)
        results["tables"][table_key] = result

        if result["status"] == "PASS":
            print(f"  Status: PASS ({result['passed_cells']}/{result['total_cells']} cells)")
        elif result["status"] == "SKIP":
            print(f"  Status: SKIP - {result['failures'][0]}")
        else:
            print("  Status: FAIL")
            for failure in result["failures"][:5]:  # Show first 5 failures
                print(f"    - {failure}")
            if len(result["failures"]) > 5:
                print(f"    ... and {len(result['failures']) - 5} more failures")
            results["overall_status"] = "FAIL"

    # Also check Tables 1 and 2 (pumping data from step1)
    pumping_tables = [
        ("2024_Table_1_updated.xlsx", "Table_1_expected.xlsx", "Table_1"),
        ("2024_Table_2_output.xlsx", "Table_2_expected.xlsx", "Table_2"),
    ]

    pumping_output_dir = PROJECT_ROOT / "output" / "ingested_data"

    for generated_name, expected_name, table_key in pumping_tables:
        actual_path = pumping_output_dir / generated_name
        expected_path = EXPECTED_DIR / expected_name

        tol_config = tolerances.get("tables", {}).get(table_key, {})

        print(f"\nComparing {table_key}...")
        print(f"  Generated: {actual_path}")
        print(f"  Expected:  {expected_path}")

        result = compare_excel_files(actual_path, expected_path, table_key, tol_config)
        results["tables"][table_key] = result

        if result["status"] == "PASS":
            print(f"  Status: PASS ({result['passed_cells']}/{result['total_cells']} cells)")
        elif result["status"] == "SKIP":
            print(f"  Status: SKIP - {result['failures'][0]}")
        else:
            print("  Status: FAIL")
            for failure in result["failures"][:5]:
                print(f"    - {failure}")
            if len(result["failures"]) > 5:
                print(f"    ... and {len(result['failures']) - 5} more failures")
            results["overall_status"] = "FAIL"

    return results


# =============================================================================
# REPORT GENERATION (Step 4)
