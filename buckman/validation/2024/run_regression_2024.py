# HASH VERIFICATION (Step 1)
# =============================================================================

def compute_sha256(file_path: Path) -> str:
    """Compute SHA-256 hash of a file."""
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            sha256_hash.update(chunk)
    return sha256_hash.hexdigest()


def verify_input_hashes() -> bool:
    """
    Verify SHA-256 hashes of frozen input files match hashes.json.

    Returns:
        True if all hashes match, False otherwise
    """
    print_header("STEP 1: VERIFY INPUT HASHES")

    hashes_file = INPUTS_DIR / "hashes.json"
    if not hashes_file.exists():
        print_error(
            "hashes.json not found",
            str(hashes_file),
            "File missing",
            "hashes.json with SHA-256 hashes",
            "Run validation setup first"
        )
        return False

    with open(hashes_file) as f:
        hash_data = json.load(f)

    files_info = hash_data.get("files", {})
    all_passed = True

    print(f"Checking {len(files_info)} frozen input file(s)...")
    print()

    for filename, info in files_info.items():
        file_path = INPUTS_DIR / filename
        expected_hash = info["sha256"]
        expected_size = info["size_bytes"]

        if not file_path.exists():
            print(f"  FAIL: {filename}")
            print("        File not found")
            all_passed = False
            continue

        actual_size = file_path.stat().st_size
        if actual_size != expected_size:
            print(f"  FAIL: {filename}")
            print(f"        Size mismatch: {actual_size} bytes (expected {expected_size})")
            all_passed = False
            continue

        actual_hash = compute_sha256(file_path)
        if actual_hash != expected_hash:
            print(f"  FAIL: {filename}")
            print("        Hash mismatch!")
            print(f"        Expected: {expected_hash}")
            print(f"        Actual:   {actual_hash}")
            all_passed = False
            continue

        print(f"  PASS: {filename} ({actual_size:,} bytes)")

    print()
    if all_passed:
        print("All input hashes verified.")
    else:
        print("INPUT VERIFICATION FAILED - frozen inputs may have been modified!")

    return all_passed


