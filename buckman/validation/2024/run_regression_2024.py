def generate_report(
    hash_result: bool,
    pipeline_result: bool | None,
    compare_result: dict[str, Any] | None
) -> str:
    """
    Generate a text report of regression test results.

    Args:
        hash_result: True if hash verification passed
        pipeline_result: True if pipeline ran successfully, None if skipped
        compare_result: Table comparison results, None if skipped

    Returns:
        Formatted report string
    """
    print_header("REGRESSION TEST REPORT")

    lines = [
        "=" * 70,
        "2024 REGRESSION TEST REPORT",
        f"Generated: {datetime.now().isoformat()}",
        "=" * 70,
        "",
    ]

    # Hash verification
    lines.append("INPUT VERIFICATION")
    lines.append("-" * 40)
    lines.append(f"  Frozen inputs: {'PASS' if hash_result else 'FAIL'}")
    lines.append("")

    # Pipeline execution
    lines.append("PIPELINE EXECUTION")
    lines.append("-" * 40)
    if pipeline_result is None:
        lines.append("  Status: SKIPPED")
    elif pipeline_result:
        lines.append("  Status: PASS (all steps completed)")
    else:
        lines.append("  Status: FAIL (pipeline error)")
    lines.append("")

    # Table comparison
    lines.append("TABLE COMPARISON")
    lines.append("-" * 40)
    if compare_result is None:
        lines.append("  Status: SKIPPED")
    else:
        for table_name, result in compare_result.get("tables", {}).items():
            status = result["status"]
            if status == "PASS":
                lines.append(f"  {table_name}: PASS ({result['passed_cells']}/{result['total_cells']} cells)")
            elif status == "SKIP":
                lines.append(f"  {table_name}: SKIP")
            else:
                lines.append(f"  {table_name}: FAIL ({len(result['failures'])} mismatches)")
    lines.append("")

    # Overall result
    lines.append("=" * 70)
    overall_pass = (
        hash_result and
        (pipeline_result is None or pipeline_result) and
        (compare_result is None or compare_result["overall_status"] == "PASS")
    )

    if overall_pass:
        lines.append("OVERALL RESULT: PASS")
        lines.append("")
        lines.append("The 2024 regression test passed. Pipeline outputs match expected values.")
    else:
        lines.append("OVERALL RESULT: FAIL")
        lines.append("")
        lines.append("The 2024 regression test FAILED. Review failures above.")

    lines.append("=" * 70)

    report = "\n".join(lines)
    print(report)
    return report


# =============================================================================
# MAIN ENTRY POINT
# =============================================================================

def main() -> int:
    """
    Main entry point for regression testing.

    Returns:
        Exit code: 0 if all tests passed, 1 if any failed
    """
    parser = argparse.ArgumentParser(
        description="2024 Regression Harness for Buckman Wellfield Pipeline"
    )
    parser.add_argument(
        "--verify",
        action="store_true",
        help="Only verify input hashes (skip pipeline and comparison)"
    )
    parser.add_argument(
        "--compare",
        action="store_true",
        help="Only compare tables (skip pipeline execution)"
    )
    args = parser.parse_args()

    print("=" * 70)
    print("2024 REGRESSION HARNESS")
    print(f"Started: {datetime.now().isoformat()}")
    print("=" * 70)

    # Step 1: Always verify hashes
    hash_result = verify_input_hashes()

    if args.verify:
        # Early exit for hash-only mode
        generate_report(hash_result, None, None)
        return 0 if hash_result else 1

    # Step 2: Run pipeline (unless --compare)
    pipeline_result = None
    if not args.compare:
        if hash_result:
            pipeline_result = run_pipeline_2024()
        else:
            print("\nSkipping pipeline due to hash verification failure.")
            pipeline_result = False

    # Step 3: Compare tables
    compare_result = None
    if hash_result and (args.compare or pipeline_result):
        compare_result = compare_tables()

    # Step 4: Generate report
    generate_report(hash_result, pipeline_result, compare_result)

    # Determine exit code
    overall_pass = (
        hash_result and
        (pipeline_result is None or pipeline_result) and
        (compare_result is None or compare_result["overall_status"] == "PASS")
    )

    return 0 if overall_pass else 1


if __name__ == "__main__":
    sys.exit(main())
